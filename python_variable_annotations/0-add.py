#!/usr/bin/env python3
"""A simple Python module to perform addition.

This module provides a basic function to add two floating point numbers
and return the result.
"""


def add(a: float, b: float) -> float:
    """Add two floating point numbers and return the sum.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
