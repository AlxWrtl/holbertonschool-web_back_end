#!/usr/bin/env python3
"""A simple Python module to convert numbers to strings.

This module provides a function to convert a floating point number into its
string representation.
"""


def to_str(n: float) -> str:
    """Convert a floating point number to its string representation.

    Args:
        n (float): The number to convert.

    Returns:
        str: The string representation of the number.
    """
    return str(n)
