#!/usr/bin/env python3
"""A simple Python module to perform string concatenation.

This module provides a function to concatenate two strings and return the
combined result.
"""


def concat(str1: str, str2: str):
    """Concatenate two strings and return the combined result.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        tuple: A tuple containing both strings.
    """
    return str1, str2
